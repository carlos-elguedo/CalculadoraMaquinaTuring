    private void botonOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonOKActionPerformed
        
        
        if(controlEntradas.puedeRealizarCalculo(cadena_en_cuestion)){
            //La cadena es correcta para realizar el calculo
            //controlador.inicarPrograma(cadena_en_cuestion);
            System.out.println("Realizara el calculo de: " + cadena_en_cuestion);
            
            JFrame ventana_animacion = new JFrame("SimulaciÃ³n de cinta para la operaciÃ³n: " + cadena_en_cuestion);

            ventana_animacion.setIconImage(icon);

            ventana_animacion.setLayout(null);
            //ventana_animacion.setDefaultCloseOperation(JFrame.);
            //ventana_principal.setResizable(false);
            ventana_animacion.setContentPane((Container)new MaquinaDeTuring(cadena_en_cuestion));
            ventana_animacion.setSize(1000,600);
            ventana_animacion.setLocationRelativeTo(null);
            ventana_animacion.setVisible(true);
            
            cadena_en_cuestion = "";
            limpiarPantallaEntradas();
            
        }else{
            testoConsola.setText("No se puede resolver la operacion indicada, revise su sintaxis");
            new BorrarConsola().start();
        }
        
        
        
        
        
        
    }//GEN-LAST:event_botonOKActionPerformed

